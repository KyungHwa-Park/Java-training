package exercise;

public class Q5 {

	public static void main(String[] args) {
		int num1 = 2;
		int num2 = 10;
		
		// 비트 논리 연산자
		System.out.println(num1 & num2); 		// 0010(2) => 2
		System.out.println(num1 | num2); 		// 1010(2) => 10
		System.out.println(num1 ^ num2); 		// 1000(2) => 8
		System.out.println(~num1);				// -3

	}

}
